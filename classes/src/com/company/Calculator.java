package com.company;

public class Calculator {
    public int addition(int num1, int num2){
        return num1+num2;
    }

    public int substraction(int num1, int num2){
        return num1-num2;
    }

    public int multipication(int num1, int num2){
        return num1*num2;
    }

    public int division(int num1, int num2){
        return num1/num2;
    }
}


